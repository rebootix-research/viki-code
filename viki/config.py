settings = Settings()


def refresh_settings() -> Settings:
    fresh = Settings()
    for field_name in Settings.model_fields:
        setattr(settings, field_name, getattr(fresh, field_name))
    return settings


def write_user_config(values: Mapping[str, Optional[str]], *, remove_empty: bool = True) -> Path:
    root = user_config_root()
    root.mkdir(parents=True, exist_ok=True)
    config_file = user_config_path()
    existing = _parse_env_file(config_file)

    merged = dict(existing)
    for key, value in values.items():
        if value is None and remove_empty:
            merged.pop(key, None)
            continue
        if value is None:
            continue
        merged[key] = str(value)

    tmp_path = config_file.with_suffix(".tmp")
    tmp_path.write_text(_serialize_env_file(merged), encoding="utf-8")
    _apply_file_mode(tmp_path)
    tmp_path.replace(config_file)
    _apply_file_mode(config_file)
    refresh_settings()
    return config_file


def read_user_config() -> dict[str, str]:
    return _parse_env_file(user_config_path())

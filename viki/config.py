APP_NAME = "viki-code"
APP_AUTHOR = "Rebootix"
USER_CONFIG_FILENAME = "config.env"


def user_config_root() -> Path:
    override = os.environ.get("VIKI_CONFIG_HOME", "").strip()
    if override:
        return Path(override).expanduser().resolve()
    return Path(user_config_dir(APP_NAME, APP_AUTHOR)).resolve()


def user_config_path() -> Path:
    return user_config_root() / USER_CONFIG_FILENAME


class VikiDotEnvSettingsSource(DotEnvSettingsSource):
    def __init__(self, settings_cls: type[BaseSettings]) -> None:
        super().__init__(
            settings_cls,
            env_file=(str(user_config_path()), ".env"),
            env_file_encoding="utf-8",
        )


def _parse_env_file(path: Path) -> dict[str, str]:
    if not path.exists():
        return {}
    parsed: dict[str, str] = {}
    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        key = key.strip()
        value = value.strip()
        if value and value[0] == value[-1] and value[0] in {'"', "'"}:
            value = value[1:-1]
        parsed[key] = value
    return parsed


def _serialize_env_file(values: Mapping[str, str]) -> str:
    lines = [
        "# VIKI Code user configuration",
        "# Generated by the guided setup flow.",
        "",
    ]
    for key in sorted(values):
        value = values[key]
        if value is None:
            continue
        needs_quotes = any(ch.isspace() for ch in value) or any(ch in value for ch in ['"', "'", "#"])
        rendered = shlex.quote(value) if needs_quotes else value
        lines.append(f"{key}={rendered}")
    lines.append("")
    return "\n".join(lines)


def _apply_file_mode(path: Path) -> None:
    try:
        if os.name != "nt":
            path.chmod(stat.S_IRUSR | stat.S_IWUSR)
        else:
            os.chmod(path, stat.S_IREAD | stat.S_IWRITE)
    except OSError:
        pass


